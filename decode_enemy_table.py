#!/usr/bin/env python3
"""Reconstruct ENEMY_TYPE_TABLE from Famicom ROM.

EntityTypeTable  $E4EC  35x4 bytes: entity type byte per (stage, typeSlot)
StageEnemyCountTable $E578  35x4 bytes: count per (stage, typeSlot)

Entity type byte -> enemy type:
  $80 -> 0 (basic tank)
  $A0 -> 1 (fast tank)
  $C0 -> 2 (power tank)
  $E0 -> 3 (armor tank)

Spawn order: emit all of typeSlot 0 first (count[0] enemies), then slot 1, etc.
"""

# EntityTypeTable $E4EC, 140 bytes (35 stages x 4 type slots each)
# Extracted from: python decode_tables.py C E4EC 140 u8
ENTITY_TYPE_BYTES = [
    0x80,0xA0,0xC0,0xE0,  # stage 1
    0xE0,0xA0,0xC0,0x80,  # stage 2
    0x80,0xA0,0xC0,0xE0,  # stage 3
    0xC0,0xA0,0x80,0xE0,  # stage 4
    0xC0,0xE0,0x80,0xA0,  # stage 5
    0xC0,0xA0,0x80,0xE0,  # stage 6
    0x80,0xA0,0xC0,0x80,  # stage 7  (two basic slots: slots 0&3 both $80)
    0xC0,0xE0,0xA0,0x80,  # stage 8
    0x80,0xA0,0xC0,0xE0,  # stage 9
    0x80,0xA0,0xC0,0xE0,  # stage 10
    0xA0,0xE0,0xC0,0xA0,  # stage 11 (two fast slots: slots 0&3 both $A0)
    0xC0,0xA0,0x80,0xE0,  # stage 12
    0xC0,0xA0,0x80,0xE0,  # stage 13
    0xC0,0xA0,0x80,0xE0,  # stage 14
    0x80,0xC0,0xA0,0xE0,  # stage 15
    0x80,0xC0,0xA0,0xE0,  # stage 16
    0xE0,0xA0,0xC0,0x80,  # stage 17
    0xE0,0x80,0xC0,0xA0,  # stage 18
    0xA0,0xE0,0x80,0xC0,  # stage 19
    0xA0,0x80,0xC0,0xE0,  # stage 20
    0xC0,0xA0,0x80,0xE0,  # stage 21
    0xA0,0x80,0xC0,0xE0,  # stage 22
    0xE0,0x80,0xC0,0xA0,  # stage 23
    0xC0,0xE0,0xA0,0x80,  # stage 24
    0xC0,0xA0,0x80,0xE0,  # stage 25
    0xA0,0xE0,0x80,0xC0,  # stage 26
    0xC0,0xE0,0xA0,0x80,  # stage 27
    0xA0,0xE0,0x80,0xC0,  # stage 28
    0xC0,0xA0,0x80,0xE0,  # stage 29
    0x80,0xA0,0xC0,0xE0,  # stage 30
    0xC0,0xA0,0xE0,0xC0,  # stage 31 (two power slots: slots 0&3 both $C0)
    0xE0,0x80,0xC0,0xA0,  # stage 32
    0xA0,0xE0,0xC0,0xA0,  # stage 33 (two fast slots: slots 0&3 both $A0)
    0xC0,0xA0,0x80,0xE0,  # stage 34
    0xC0,0xA0,0x80,0xE0,  # stage 35
]

# StageEnemyCountTable $E578, 140 bytes (35 stages x 4 type slots each)
# Extracted from: python decode_tables.py C E578 140 u8
COUNT_BYTES = [
    0x12,0x02,0x00,0x00,  # stage 1:  18+2+0+0=20
    0x02,0x04,0x00,0x0E,  # stage 2:  2+4+0+14=20
    0x0E,0x04,0x00,0x02,  # stage 3:  14+4+0+2=20
    0x0A,0x05,0x02,0x03,  # stage 4:  10+5+2+3=20
    0x05,0x02,0x08,0x05,  # stage 5:  5+2+8+5=20
    0x07,0x02,0x09,0x02,  # stage 6:  7+2+9+2=20
    0x03,0x04,0x06,0x07,  # stage 7:  3+4+6+7=20
    0x07,0x02,0x04,0x07,  # stage 8:  7+2+4+7=20
    0x06,0x04,0x07,0x03,  # stage 9:  6+4+7+3=20
    0x0C,0x02,0x04,0x02,  # stage 10: 12+2+4+2=20
    0x05,0x06,0x04,0x05,  # stage 11: 5+6+4+5=20
    0x08,0x06,0x00,0x06,  # stage 12: 8+6+0+6=20
    0x08,0x08,0x00,0x04,  # stage 13: 8+8+0+4=20
    0x0A,0x04,0x00,0x06,  # stage 14: 10+4+0+6=20
    0x02,0x00,0x0A,0x08,  # stage 15: 2+0+10+8=20
    0x10,0x00,0x02,0x02,  # stage 16: 16+0+2+2=20
    0x02,0x02,0x08,0x08,  # stage 17: 2+2+8+8=20
    0x04,0x02,0x06,0x08,  # stage 18: 4+2+6+8=20
    0x04,0x08,0x04,0x04,  # stage 19: 4+8+4+4=20
    0x08,0x02,0x02,0x08,  # stage 20: 8+2+2+8=20
    0x08,0x02,0x06,0x04,  # stage 21: 8+2+6+4=20
    0x08,0x06,0x02,0x04,  # stage 22: 8+6+2+4=20
    0x06,0x00,0x04,0x0A,  # stage 23: 6+0+4+10=20
    0x04,0x02,0x04,0x0A,  # stage 24: 4+2+4+10=20
    0x02,0x08,0x00,0x0A,  # stage 25: 2+8+0+10=20
    0x06,0x06,0x04,0x04,  # stage 26: 6+6+4+4=20
    0x02,0x08,0x08,0x02,  # stage 27: 2+8+8+2=20
    0x02,0x01,0x0F,0x02,  # stage 28: 2+1+15+2=20
    0x0A,0x04,0x00,0x06,  # stage 29: 10+4+0+6=20
    0x04,0x08,0x04,0x04,  # stage 30: 4+8+4+4=20
    0x03,0x08,0x06,0x03,  # stage 31: 3+8+6+3=20
    0x08,0x06,0x02,0x04,  # stage 32: 8+6+2+4=20
    0x04,0x08,0x04,0x04,  # stage 33: 4+8+4+4=20
    0x04,0x0A,0x00,0x06,  # stage 34: 4+10+0+6=20
    0x04,0x06,0x00,0x0A,  # stage 35: 4+6+0+10=20
]

def byte_to_type(b):
    """Convert entity type byte ($80/$A0/$C0/$E0) to enemy type index 0-3."""
    return (b - 0x80) >> 5

def reconstruct_stage(stage_0idx):
    """Reconstruct the 20-slot enemy sequence for stage (0-indexed)."""
    base = stage_0idx * 4
    types = [byte_to_type(ENTITY_TYPE_BYTES[base + s]) for s in range(4)]
    counts = [COUNT_BYTES[base + s] for s in range(4)]
    total = sum(counts)

    sequence = []
    for slot in range(4):
        sequence.extend([types[slot]] * counts[slot])

    return sequence, types, counts, total

# Validate totals
errors = 0
for s in range(35):
    _, _, counts, total = reconstruct_stage(s)
    if total != 20:
        print(f"ERROR: stage {s+1} total={total} (expected 20) counts={counts}")
        errors += 1
if errors == 0:
    print("All 35 stages sum to 20 enemies. OK")
print()

# Print reconstruction
print("Famicom ENEMY_TYPE_TABLE (type 0=basic 1=fast 2=power 3=armor):")
print("=" * 70)
all_stages = []
for s in range(35):
    seq, types, counts, total = reconstruct_stage(s)
    all_stages.append(seq)
    type_names = ['B','F','P','A']
    slots_str = " ".join(f"{type_names[types[i]]}x{counts[i]}" for i in range(4) if counts[i]>0)
    print(f"Stage {s+1:2d}: {slots_str:30s}  {seq}")

print()
print("JavaScript ENEMY_TYPE_TABLE (paste into game.js):")
print("const ENEMY_TYPE_TABLE = [")
for s in range(35):
    seq = all_stages[s]
    print(f"  {seq},  // stage {s+1}")
print("];")
