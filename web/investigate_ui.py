    (0x00,0x40,0x4d), (0x00,0x00,0x00), (0x00,0x00,0x00), (0x00,0x00,0x00),
    (0xad,0xad,0xad), (0x15,0x5f,0xd9), (0x42,0x40,0xff), (0x75,0x27,0xfe),
