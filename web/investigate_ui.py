def decode_tile(bank_data, tile_idx):
    offset = tile_idx * 16
    pixels = []
    for row in range(8):
        p0 = bank_data[offset + row]
        p1 = bank_data[offset + row + 8]
        for bit in range(7, -1, -1):
            c = ((p0 >> bit) & 1) | (((p1 >> bit) & 1) << 1)
            pixels.append(c)
    return pixels

def main():
    if not os.path.exists(ROM_PATH):
        print(f"Error: {ROM_PATH} not found")
        sys.exit(1)

    with open(ROM_PATH, 'rb') as f:
        rom = f.read()

    print("=== Palettes (at $D44A) ===")
    pal_off = 0xD44A
    for i in range(8):
        bytes_ = rom[pal_off + i*4 : pal_off + (i+1)*4]
        hex_ = ' '.join(f'${b:02X}' for b in bytes_)
        print(f"  Palette {i}: {hex_}")
        for b in bytes_:
            rgb = NES_RGB[b & 0x3F]
            print(f"    ${b:02X} -> RGB({rgb[0]},{rgb[1]},{rgb[2]})")

    print("\n=== Scanning for Solid Tiles (all pixels same color) ===")
    prg_banks = rom[4]
    chr_off = 16 + prg_banks * 16384
    chr_data = rom[chr_off : chr_off + 16384]
    
    for b_idx in range(4):
        bank = chr_data[b_idx*4096 : (b_idx+1)*4096]
        solids = []
        for t_idx in range(256):
            pixels = decode_tile(bank, t_idx)
            if all(p == pixels[0] for p in pixels):
                solids.append((t_idx, pixels[0]))
        
        if solids:
            print(f"  Bank {b_idx}:")
            for t_idx, color in solids:
                print(f"    Tile ${t_idx:02X}: Solid Color {color}")

    print("\n=== Special Tiles Analysis ===")
    # Check tile $FC specifically
    for b_idx in range(4):
        bank = chr_data[b_idx*4096 : (b_idx+1)*4096]
        pixels = decode_tile(bank, 0xFC)
        unique = set(pixels)
        print(f"  Bank {b_idx} Tile $FC uses colors: {sorted(list(unique))}")

if __name__ == "__main__":
    main()
