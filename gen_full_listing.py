def main():
    rom = NESRom(ROM_FILE)
    labels = load_knowledge(LABELS_FILE)
    comments = load_knowledge(COMMENTS_FILE)

    print(f"; VS. Battle City — Full ROM Listing")
    print(f"; Generated by gen_full_listing.py")
    print()

    for bank, start, end, rtype, metadata in RANGES:
        print(f"; ---------------------------------------------------------------------------")
        header = f"; Range ${start:04X}–${end:04X} ({rtype})"
        if metadata: header += f" {metadata}"
        print(header)
        print(f"; ---------------------------------------------------------------------------")

        addr = start
        while addr < end:
            # Labels and comments
            lbl = get_kv(labels, bank, addr)
            if lbl: print(f"\n{lbl}:")
            cmt = get_kv(comments, bank, addr)
            cmt_s = f" ; {cmt}" if cmt else ""

            if rtype == 'code':
                off = rom.cpu_to_prg_offset(addr)
                if off >= len(rom.prg): break
                mnemonic, mode, length, cycles, operand = decode(rom.prg, off)
                byte_str = ' '.join(f'{b:02X}' for b in rom.prg[off:off+length])
                
                # Resolving labels for operands
                bank_labels = {a: n for (b, a), n in labels.items() if b is None or b == bank}
                op_str = format_operand(mode, operand, addr + length, bank_labels)
                
                print(f"  ${addr:04X}  {byte_str:<8}  {mnemonic} {op_str}{cmt_s}")
                addr += length
            elif rtype == 'ptr16' or rtype == 'u16':
                off = rom.cpu_to_prg_offset(addr)
                if off + 1 >= len(rom.prg): break
                if rtype == 'ptr16': line = fmt_ptr16(rom.prg[off:off+2], addr, labels, bank)
                else: line = fmt_u16(rom.prg[off:off+2], addr, labels, bank)
                print(f"{line}{cmt_s}")
                addr += 2
            elif rtype in ('u8', 's8', 'hex8'):
                off = rom.cpu_to_prg_offset(addr)
                if off >= len(rom.prg): break
                if rtype == 'u8': line = fmt_u8(rom.prg[off:off+1], addr, labels, bank)
                elif rtype == 's8': line = fmt_s8(rom.prg[off:off+1], addr, labels, bank)
                else: line = fmt_hex8(rom.prg[off:off+1], addr, labels, bank)
                print(f"{line}{cmt_s}")
                addr += 1
            else:
                addr += 1

    # CHR-ROM dump
    print()
    print(f"; ---------------------------------------------------------------------------")
    print(f"; CHR-ROM ({len(rom.chr)} bytes)")
    print(f"; ---------------------------------------------------------------------------")
    for bidx in range(rom.chr_banks * 2): # 4KB banks
        start = bidx * 4096
        end = start + 4096
        print(f"\n; CHR Bank {bidx} ($0000-$0FFF in PPU space if selected)")
        for i in range(start, end, 16):
            chunk = rom.chr[i:i+16]
            hex_s = ' '.join(f'{b:02X}' for b in chunk)
            print(f"  {i:04X}  {hex_s}")

if __name__ == "__main__":
    main()
