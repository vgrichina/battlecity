class NESRom:
    def __init__(self, path):
        with open(path, 'rb') as f:
            self.raw = f.read()
        if self.raw[:4] != b'NES\x1a':
            raise ValueError("Not a valid iNES ROM")
        self.prg_banks = self.raw[4]
        self.chr_banks = self.raw[5]
        self.has_trainer = bool(self.raw[6] & 0x04)
        self.prg_start = 16 + (512 if self.has_trainer else 0)
        self.prg_size = self.prg_banks * 16384
        self.prg = self.raw[self.prg_start : self.prg_start + self.prg_size]
        self.chr_start = self.prg_start + self.prg_size
        self.chr_size = self.chr_banks * 8192
        self.chr = self.raw[self.chr_start : self.chr_start + self.chr_size]

    def cpu_to_prg_offset(self, cpu_addr):
        return cpu_addr - 0x8000

def load_knowledge(path):
    data = {}
    if not os.path.exists(path):
        return data
    import csv
    with open(path, newline='') as f:
        for row in csv.reader(f):
            if not row or row[0].startswith('#'): continue
            if len(row) < 3: continue
            bank_s, addr_s, val = row[0].strip(), row[1].strip(), row[2].strip()
            try:
                bank = int(bank_s, 16) if bank_s not in ('', '*') else None
                addr = int(addr_s, 16)
                data[(bank, addr)] = val
            except ValueError: pass
    return data

def get_kv(data, bank, addr):
    return data.get((bank, addr)) or data.get((None, addr))

# Formatting helpers
def fmt_hex8(data, addr, labels, bank):
    return f"  ${addr:04X}  {data[0]:02X}        .byte ${data[0]:02X}"

def fmt_u8(data, addr, labels, bank):
    return f"  ${addr:04X}  {data[0]:02X}        .byte ${data[0]:02X}  ; {data[0]:d}"

def fmt_s8(data, addr, labels, bank):
    v = data[0]
    sv = v if v < 128 else v - 256
    return f"  ${addr:04X}  {v:02X}        .byte ${v:02X}  ; {sv:+d}"

def fmt_ptr16(data, addr, labels, bank):
    ptr = data[0] | (data[1] << 8)
    lbl = get_kv(labels, bank, ptr)
    note = f" -> {lbl}" if lbl else ""
    return f"  ${addr:04X}  {data[0]:02X} {data[1]:02X}     .word ${ptr:04X}{note}"

def fmt_u16(data, addr, labels, bank):
    v = data[0] | (data[1] << 8)
    return f"  ${addr:04X}  {data[0]:02X} {data[1]:02X}     .word ${v:04X}  ; {v:d}"

